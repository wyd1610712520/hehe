#import "MenuView.h"

@implementation MenuView

UIImage *_backgroundImage;

+ (void)initialize{
    _backgroundImage = [UIImage imageNamed:@"nav_alert_background.png"];
    
}

- (id)init{
    self = [super init];
    if (self) {
        CGRect frame = self.frame;
        frame.size.width = _backgroundImage.size.width;
        frame.size.height = _backgroundImage.size.height;
        self.frame = frame;
    }
    return self;
}

@end
